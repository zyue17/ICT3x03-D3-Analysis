class User():
    def __init_(self):
        self.name =""
        self.address=""
        self.email=""
        self.phone=""
        self.pw=""
        self.balance

    def setName(self,name):
        self.name = name

    def setAddress(self,Address):
        self.Address = Address

    def setPhone(self,phone):
        self.phone = phone

    def setEmail(self,email):
        self.email = email

    def setPw(self,pw):
        self.pw = pw

    def setBalance(self,balance):
        self.balance = balance

    def getName(self):
        self.name

    def getAddress(self):
        return self.Address

    def getPhone(self):
        return self.phone

    def getEmail(self):
        return self.email

    def getPw(self):
        return self.pw

    def getBalance(self):
        return self.balance
